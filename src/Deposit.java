    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        User u = AccountPIN.user;
        try {
            int amount = Integer.parseInt(deposit.getText());
            
            if (amount <= 0) {
                JOptionPane.showMessageDialog(this, "Enter a positive number!");
            }
            else {
                JOptionPane.showMessageDialog(this, "Amount Deposited: "+amount);
                u.deposit(amount);
            
            }
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Enter an integer");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

